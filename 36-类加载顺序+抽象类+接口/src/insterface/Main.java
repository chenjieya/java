package insterface;

public class Main {
    public static void main(String[] args) {
        /**
         * 接口
         *
         * 抽象类抽象到极致就是接口（里面的内容必须全部都是抽象的）
         *
         * 接口只能包含：
         *  公共静态不可变的属性 public static final
         *  公共抽象方法 public abstract
         *
         * 不能包含块，构造函数
         * 没有构造函数也不能创建对象
         *
         * 使用方法：
         *  只能通过子类多实现（implements）来做事情
         *      public class A implements B,C,D {}
         *
         */
    }
}
